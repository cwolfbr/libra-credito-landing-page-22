// Funções utilitárias

/**
 * Busca dados de LTV para uma cidade específica
 */
export function getCityLtv(cidade: string, uf: string): CityLtvData | null {
  const normalizedCidade = cidade.trim().toLowerCase();
  const normalizedUf = uf.trim().toUpperCase();
  
  return CITY_LTV_DATA.find(
    item => 
      item.cidade.toLowerCase() === normalizedCidade && 
      item.uf === normalizedUf
  ) || null;
}

/**
 * Valida se um empréstimo é permitido para uma cidade específica
 */
export function validateLoanForCity(
  cidade: string,
  uf: string,
  loanAmount: number,
  guaranteeAmount: number,
  isRural: boolean = false
): LtvValidationResult {
  const cityData = getCityLtv(cidade, uf);
  
  if (!cityData) {
    return {
      isValid: false,
      error: 'Esta cidade não está em nossa área de cobertura'
    };
  }

  const ltvConfig = LTV_CONFIG[cityData.ltv as keyof typeof LTV_CONFIG];
  
  if (!ltvConfig) {
    return {
      isValid: false,
      error: 'Configuração de LTV não encontrada para esta cidade'
    };
  }

  // Cidade não atendida (LTV 0)
  if (cityData.ltv === 0) {
    return {
      isValid: false,
      error: 'Esta cidade não está em nossa área de cobertura'
    };
  }

  // Calcular LTV atual
  const currentLtv = (loanAmount / guaranteeAmount) * 100;
  const maxLtv = ltvConfig.maxLtv;
  const maxLoanAmount = (guaranteeAmount * maxLtv) / 100;

  // Validar se o empréstimo está dentro do limite
  if (currentLtv <= maxLtv) {
    return {
      isValid: true,
      maxLoanAmount,
      currentLtv,
      suggestedLoanAmount: maxLoanAmount
    };
  } else {
    return {
      isValid: false,
      error: `Valor do empréstimo excede o limite de ${maxLtv}% do valor do imóvel. Máximo permitido: R$ ${maxLoanAmount.toLocaleString('pt-BR', { minimumFractionDigits: 2 })}`,
      maxLoanAmount,
      currentLtv,
      suggestedLoanAmount: maxLoanAmount
    };
  }
}

/**
 * Busca cidades por nome (para autocomplete)
 */
export function searchCities(query: string, limit: number = 10): CityLtvData[] {
  if (query.length < 2) return [];
  
  const normalizedQuery = query.toLowerCase();
  
  return CITY_LTV_DATA
    .filter(city => 
      city.cidade.toLowerCase().includes(normalizedQuery)
    )
    .sort((a, b) => {
      // Priorizar cidades que começam com a busca
      const aStartsWith = a.cidade.toLowerCase().startsWith(normalizedQuery);
      const bStartsWith = b.cidade.toLowerCase().startsWith(normalizedQuery);
      
      if (aStartsWith && !bStartsWith) return -1;
      if (!aStartsWith && bStartsWith) return 1;
      
      // Depois ordenar por LTV (maior primeiro) e nome
      if (a.ltv !== b.ltv) return b.ltv - a.ltv;
      return a.cidade.localeCompare(b.cidade);
    })
    .slice(0, limit);
}

/**
 * Obtém estatísticas do dataset
 */
export function getDatasetStats() {
  const stats = {
    total: CITY_LTV_DATA.length,
    ltv0: CITY_LTV_DATA.filter(c => c.ltv === 0).length,
    ltv30: CITY_LTV_DATA.filter(c => c.ltv === 30).length,
    ltv50: CITY_LTV_DATA.filter(c => c.ltv === 50).length,
    byState: {} as Record<string, number>
  };

  CITY_LTV_DATA.forEach(city => {
    stats.byState[city.uf] = (stats.byState[city.uf] || 0) + 1;
  });

  return stats;
}

/**
 * Calcula sugestão de valor máximo do empréstimo
 */
export function getSuggestedLoanAmount(
  cidade: string,
  uf: string,
  guaranteeAmount: number,
  isRural: boolean = false
): number {
  const cityData = getCityLtv(cidade, uf);
  
  if (!cityData || cityData.ltv === 0) {
    return 0;
  }

  const ltvConfig = LTV_CONFIG[cityData.ltv as keyof typeof LTV_CONFIG];
  const maxLtv = ltvConfig.maxLtv;
  
  return (guaranteeAmount * maxLtv) / 100;
}